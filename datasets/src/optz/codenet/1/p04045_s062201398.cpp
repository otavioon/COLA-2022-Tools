using namespace std;
const int mod=1000000009;
int vis[10];
int main()
{
    memset(vis,0,sizeof(vis));
    int n,k,m;
    string s;
    cin>>s;
    cin>>k;
    int flag=0;
    for(int i=1; i<=k; i++)
        cin>>m,vis[m]=1;
    for(int i=0; i<s.size(); i++)
    {
        if(vis[s[i]-'0']==1)
        {
            if(!flag)
                for(int j=0; j<=9; j++)
                    if(vis[j]==0&&j>=s[i]-'0')
                    {
                        s[i]=j+'0';
                        flag=1;
                        break;
                    }
                    else
                        for(int j=0; j<=9; j++)
                            if(vis[j]==0)
                            {
                                s[i]=j+'0';
                                break;
                            }
        }
    }
    cout<<s<<endl;
}
