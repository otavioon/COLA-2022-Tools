int main() {
    ios::sync_with_stdio(false);
    cin.tie(0);
    int n, k; cin >> n >> k;
    vector<bool> a(10, 1);
    for (int i = 0; i < k; i++) {
        int x; cin >> x;
        a[x] = 0;
    }
    int mx = 0;
    for (int i = 0; i < 10; i++) {
        if(a[i]) {
            mx = i;
        }
    }
    int digit = 1;
    int cmx = mx;
    while(mx < n) {
        digit *= 10;
        mx *= 10;
        mx += cmx;
    }
    int ans = 0;
    int i = 0;
    while (digit > 0) {
        if (a[i])  {
            if (ans + (i * digit) + (mx / 10) >= n) {
                ans += i * digit;
                digit /= 10;
                mx /= 10;
                i = -1;
            }
        }
        i++;
    }
    cout << ans;
}
